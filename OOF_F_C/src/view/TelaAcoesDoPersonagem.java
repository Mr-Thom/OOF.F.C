    private void botaoAposentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAposentarActionPerformed
        personagem.Aposentar();
        try {
            PersonagemDAO p = PersonagemDAO.getInstance();
            ClassificacaoDAO c = ClassificacaoDAO.getInstance();
            Classificado classificado = new Classificado(personagem.getNome(), personagem.getIdade(), personagem.getPeso().getCod(), personagem.getHistorico().getPartidasJogadas(), personagem.getHistorico().getPartidasGanhas(), personagem.getHistorico().getPartidasPerdidas());
            p.editar(personagem);
            c.inserir(classificado);

            new TelaAposentado(personagem).setVisible(true);
            this.setVisible(false);
        } catch (Exception e) {
            new TelaPopUp(e.getMessage()).setVisible(true);
        }
    }//GEN-LAST:event_botaoAposentarActionPerformed

